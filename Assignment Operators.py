
# Assignment Operators in Python

# Normal assignment
x = 10
print("x =", x)

# Add and assign
x += 5   # same as x = x + 5
print("After x += 5:", x)

# Subtract and assign
x -= 3   # same as x = x - 3
print("After x -= 3:", x)

# Multiply and assign
x *= 2   # same as x = x * 2
print("After x *= 2:", x)

# Divide and assign
x /= 4   # same as x = x / 4
print("After x /= 4:", x)

# Modulus and assign
x %= 3   # same as x = x % 3
print("After x %= 3:", x)

# Floor division and assign
x //= 2  # same as x = x // 2
print("After x //= 2:", x)

# Exponent and assign
x **= 3  # same as x = x ** 3
print("After x **= 3:", x)

# Bitwise AND and assign
x = 5
x &= 3   # same as x = x & 3
print("After x &= 3:", x)

# Bitwise OR and assign
x |= 2   # same as x = x | 2
print("After x |= 2:", x)

# Bitwise XOR and assign
x ^= 1   # same as x = x ^ 1
print("After x ^= 1:", x)

# Bitwise right shift and assign
x >>= 1  # same as x = x >> 1
print("After x >>= 1:", x)

# Bitwise left shift and assign
x <<= 2  # same as x = x << 2
print("After x <<= 2:", x)