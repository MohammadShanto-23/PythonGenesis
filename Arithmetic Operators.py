
# Python arithmetic 

# Numbers
a = 15
b = 4

# Addition
add = a + b
print(f"Addition: {a} + {b} = {add}")

# Subtraction
sub = a - b
print(f"Subtraction: {a} - {b} = {sub}")

# Multiplication
mul = a * b
print(f"Multiplication: {a} * {b} = {mul}")

# Division
div = a / b
print(f"Division: {a} / {b} = {div}")

# Floor Division
floor_div = a // b
print(f"Floor Division: {a} // {b} = {floor_div}")

# Modulus
mod = a % b
print(f"Modulus: {a} % {b} = {mod}")

# Exponentiation
exp = a ** b
print(f"Exponentiation: {a} ** {b} = {exp}")

